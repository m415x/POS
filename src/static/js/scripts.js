$(document).ready(function(){
    $("#btn__edit").click(function(){
        $(".edit__input")
        .removeAttr('readonly')
        .removeAttr('disabled')
        .removeClass('form-control-plaintext')
        .addClass('form-text');
        $("#btn__edit").hide();
        $(".btn__confirm").removeClass('visually-hidden');
        $(".btn__delete").addClass('visually-hidden');
        $(".btn__file").removeClass('visually-hidden');
    })
})
